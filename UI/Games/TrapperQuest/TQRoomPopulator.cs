        public static void PopulateRoomEnemies(TQRoom room, int ID)
        {
            room.Entities.AddRange(Layouts[ID].Population);
            return; // TODO: FIX THIS, MANDATORY
            foreach (var entity in Layouts[ID].Population)
            {
                if (entity is TQRock rok)
                {
